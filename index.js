const express=require("express")
const dbConnect=require("../mock-10-server/config/connect")
const userRouter=require("../mock-10-server/features/routes/auth.router")
const emiRouter=require("../mock-10-server/features/routes/emi.route")
const cors=require("cors")
const app=express()
app.use(cors())
app.use(express.json())
app.use("/users",userRouter)
app.use("/emi",emiRouter)
const PORT=process.env.PORT||8080;
app.listen(PORT,async()=>{
   await dbConnect()
   console.log(`server started on ${PORT}`)
})